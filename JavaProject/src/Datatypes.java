
public class Datatypes {

	public static void main(String[] args) {
		byte a=120;      // Range is -128 to 127
		short b=3200;  //Range is -32,768 to 32,767
		int c=20000;   //Range is -2,147,483,648 to 2,147,483,647
		long d=200000;  //Range is -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
		float e=2.3333f; //Sufficient for storing 6 to 7 decimal digits
		double f=10.4555555; //Sufficient for storing 15 decimal digits
		char g='A'; //Stores a single character or letter 
		String h="welcome"; 
		boolean i=true; //Stores true or false
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println(i);

		
	}

}
